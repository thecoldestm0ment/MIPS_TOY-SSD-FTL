# -----------------------------------------------------------------------------
# cmd_write
#   역할  : 메뉴 1 처리. 사용자 write 요청을 받아 처리한다.
# -----------------------------------------------------------------------------
cmd_write:
        addiu $sp, $sp, -4
        sw    $ra, 0($sp)
        jal   submit_write_request
        lw    $ra, 0($sp)
        addiu $sp, $sp, 4
        jr    $ra

