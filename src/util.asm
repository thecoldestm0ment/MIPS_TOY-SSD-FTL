# =============================================================================
# util.asm  --  공통 유틸리티 함수
#
# 여기서 정의한 함수들은 다른 파일에서 jal로 호출된다.
# leaf 함수(내부에서 jal 호출 없음)는 스택을 쓰지 않는다.
# =============================================================================

        .text

# -----------------------------------------------------------------------------
# print_string
#   역할  : $a0가 가리키는 null-terminated 문자열을 출력한다
#   입력  : $a0 = 문자열 주소
#   출력  : 없음
# -----------------------------------------------------------------------------
print_string:
        li    $v0, 4
        syscall
        jr    $ra

# -----------------------------------------------------------------------------
# print_int
#   역할  : $a0에 담긴 정수를 출력한다
#   입력  : $a0 = 출력할 정수
#   출력  : 없음
# -----------------------------------------------------------------------------
print_int:
        li    $v0, 1
        syscall
        jr    $ra

# -----------------------------------------------------------------------------
# print_newline
#   역할  : 줄바꿈 한 번 출력
#   입력  : 없음
#   출력  : 없음
# -----------------------------------------------------------------------------
print_newline:
        la    $a0, msg_newline
        li    $v0, 4
        syscall
        jr    $ra

# -----------------------------------------------------------------------------
# print_separator
#   역할  : "----" 구분선 출력
#   입력  : 없음
#   출력  : 없음
# -----------------------------------------------------------------------------
print_separator:
        la    $a0, msg_separator
        li    $v0, 4
        syscall
        jr    $ra

# -----------------------------------------------------------------------------
# read_int
#   역할  : 사용자로부터 정수 하나를 읽어서 반환한다
#   입력  : 없음
#   출력  : $v0 = 입력받은 정수
# -----------------------------------------------------------------------------
read_int:
        li    $v0, 5
        syscall
        jr    $ra

# -----------------------------------------------------------------------------
# run_state
#   역할  : 상태 메시지를 출력하고, duration만큼 시뮬레이션 시간을 흘린다.
#           total_state_count와 total_simulated_time을 갱신하고
#           syscall 32로 실제 sleep을 수행한다.
#   입력  : $a0 = 출력할 메시지 주소
#           $a1 = duration (밀리초)
#   출력  : 없음
#   주의  : 내부에서 jal 없이 syscall만 쓰므로 스택은 $ra/$a0/$a1 보존용만 사용
# -----------------------------------------------------------------------------
run_state:
        addiu $sp, $sp, -12
        sw    $ra, 8($sp)
        sw    $a0, 4($sp)
        sw    $a1, 0($sp)

        la    $a0, msg_state_op
        li    $v0, 4
        syscall

        lw    $a0, 4($sp)
        li    $v0, 4
        syscall

        lw    $a0, 0($sp)
        li    $v0, 1
        syscall

        la    $a0, msg_ms
        li    $v0, 4
        syscall

        # 통계 갱신
        lw    $t0, total_state_count
        addiu $t0, $t0, 1
        sw    $t0, total_state_count

        lw    $t0, total_simulated_time
        lw    $t1, 0($sp)
        add   $t0, $t0, $t1
        sw    $t0, total_simulated_time

        # sleep
        lw    $a0, 0($sp)
        li    $v0, 32
        syscall

        lw    $ra, 8($sp)
        addiu $sp, $sp, 12
        jr    $ra
